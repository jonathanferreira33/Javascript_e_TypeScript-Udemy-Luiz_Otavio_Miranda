// For of - Estrutura de repetição
// for classico - geralmente com iteráveis (array ou strings )
// for in - retorna o indice ou chave 
// For of - Retorna o valor em si 

const nome = 'Jonathan Ferreira';

for (let valor of nome) {
    console.log(valor);
}

const nomes = ['Jonathan', 'Samara']

for (let valores of nomes) {
    console.log(valores);
}

